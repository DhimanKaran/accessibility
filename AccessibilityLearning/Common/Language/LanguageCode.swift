enum Language {
    enum Code: String {
        case en
        case hi
        case fr
        case es
        case de
        case ar
    }

    enum name: String {
        case english = "English"
        case hindi = "Hindi"
        case french = "French"
        case spanish = "español"
        case german = "German"
        case arabic = "Arabic"
    }
}
